  private async createGuildEvent(match: Match, channel: VoiceChannel): Promise<GuildScheduledEvent> {
    const guild = channel.guild;
    
    console.log(`üé≠ Event olu≈üturma detaylarƒ±:`);
    console.log(`   - Ma√ß: ${match.homeTeam.name} vs ${match.awayTeam.name}`);
    console.log(`   - Event adƒ±: ${match.homeTeam.name} - ${match.awayTeam.name}`);
    console.log(`   - Event tipi: Voice`);
    console.log(`   - Lokasyon (kanal ID): ${channel.id}`);
    console.log(`   - Ba≈ülangƒ±√ß zamanƒ±: ${match.date}`);
    console.log(`   - Gizlilik: GuildOnly`);
    
    try {
      console.log(`üöÄ Discord API'ye event olu≈üturma isteƒüi g√∂nderiliyor...`);
      const event = await guild.scheduledEvents.create({
        name: `${match.homeTeam.name} - ${match.awayTeam.name}`,
        privacyLevel: GuildScheduledEventPrivacyLevel.GuildOnly,
        entityType: GuildScheduledEventEntityType.Voice,
        entityMetadata: { location: channel.id },
        scheduledStartTime: new Date(match.date),
        description: await this.createEventDescription(match),
      });
      
      console.log(`‚úÖ Discord event ba≈üarƒ±yla olu≈üturuldu!`);
      console.log(`   - Event ID: ${event.id}`);
      console.log(`   - Event adƒ±: ${event.name}`);
      console.log(`   - Event URL: ${event.url || 'Mevcut deƒüil'}`);
      console.log(`   - Entity ID: ${event.entityId}`);
      console.log(`   - Entity Type: ${getEntityTypeString(event.entityType)}`);
      
      // Veritabanƒ±na event_id'yi kaydet
      console.log(`üíæ Veritabanƒ±na event bilgileri kaydediliyor...`);
      await this.supabase.updateMatchWithEventId(match.id, event.id);
      console.log(`   - Ma√ß tablosu g√ºncellendi: event_id = ${event.id}`);
      
      // events tablosuna kaydet
      const dbEvent = await this.supabase.createEvent(match.id, event.id);
      console.log(`   - Events tablosuna kayƒ±t eklendi: ID = ${dbEvent.id}`);
      
      return event;
    } catch (error) {
      console.error(`‚ùå Event olu≈üturma sƒ±rasƒ±nda hata olu≈ütu:`, error);
      console.error(`   - Hata detaylarƒ±: ${error instanceof Error ? error.message : 'Bilinmeyen hata'}`);
      throw error;
    }
  }

  private async createGuildEventWithRetry(match: Match, channel: VoiceChannel, maxRetries = 3): Promise<GuildScheduledEvent | null> {
    for (let i = 0; i < maxRetries; i++) {
      try {
        return await this.createGuildEvent(match, channel);
      } catch (error) {
        console.error(`Etkinlik olu≈üturulamadƒ± (deneme ${i + 1}/${maxRetries}):`, error);
        if (i === maxRetries - 1) throw error;
        await new Promise(resolve => setTimeout(resolve, 1000 * (i + 1))); // Exponential backoff
      }
    }
    return null;
  }


